package java_basic.chap_02_Operator;

public class _02_Operator2 {
    public static void main(String[] args) {
        //복합 대입연산자
        int num = 10;
        System.out.println(num);
            //result: 10
        num += 2;
        System.out.println(num);
            //result: 12
        num -= 2;
        System.out.println(num);
            //result: 10
        num *= 2;
        System.out.println(num);
            //result: 20
        num /= 2;
        System.out.println(num);
            //result: 10
        num %= 2;
        System.out.println(num);
            //result: 0
    }
}
