package java_basic.chap_02_Operator;

public class _03_Operator3 {
    public static void main(String[] args) {
        //비교연산자
        System.out.println(5 > 3); //5는 3보다 크다.
            //result: true
        System.out.println(5 >= 3); //5는 3보다 크거나 같다.
            //result: true
        System.out.println(5 >= 5); //5는 5보다 크거나 같다.
            //result: true
        System.out.println(5 >= 7); //5는 7보다 크거나 같다.
            //result: false
        System.out.println(5 == 5); //5는 5와 같다
            //result: true
        System.out.println(5 != 3); //5는 3과 같지 않다.
            //result: true
    }
}
